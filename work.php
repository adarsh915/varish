<script>
document.addEventListener("DOMContentLoaded", () => {

  const cards = document.querySelectorAll(".project-card");
  let activePreview = null;

  cards.forEach(card => {
    const preview = card.querySelector(".hover-preview");

    const videoWidth = 260;
    const videoHeight = 170;
    const padding = 10;

    const maxX = 430;   // ⭐ LEFT / RIGHT MAX LIMIT
    const maxY = 300;   // ⭐ TOP / BOTTOM MAX LIMIT

    card.addEventListener("mouseenter", () => {

      if (activePreview && activePreview !== preview) {
        gsap.set(activePreview, { opacity: 0, visibility: "hidden" });
      }

      activePreview = preview;

      gsap.to(preview, {
        opacity: 1,
        visibility: "visible",
        duration: 0.25,
        ease: "power3.out"
      });
    });

    card.addEventListener("mousemove", (e) => {
      const rect = card.getBoundingClientRect();

      let x = e.clientX - rect.left + 20;
      let y = e.clientY - rect.top + 20;

      // ⭐ clamp inside card (with padding)
      x = Math.max(padding, Math.min(x, rect.width - videoWidth - padding));
      y = Math.max(padding, Math.min(y, rect.height - videoHeight - padding));

      // ⭐ HARD LIMITS
      if (x > maxX) x = maxX;   // LEFT/RIGHT limit
      if (y > maxY) y = maxY;   // TOP/BOTTOM limit

      gsap.to(preview, {
        x: x,
        y: y,
        duration: 0.08,
        ease: "power2.out"
      });

    });

    card.addEventListener("mouseleave", () => {
      gsap.to(preview, {
        opacity: 0,
        visibility: "hidden",
        duration: 0.25,
        ease: "power3.inOut"
      });
    });

  });

});



</script>



